import tensorflow as tf

# -------------------------------------------------------

A = tf.constant(5)
B = tf.constant(6.0)
C = tf.constant([[1, 2, 3], [4, 5, 6], [7, 8, 9]])
D = tf.constant([[1, 2, 3], [4, 5, 6], [7, 8, 9]], dtype=tf.dtypes.float32)

print(A)
print(B)
print(C)
print(D)

